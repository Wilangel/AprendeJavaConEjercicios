package AprendeJavaConEjercicios.Capitulo1;
/*
 * Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su
correspondiente traducción al castellano. Las palabras deben estar distribuidas
en dos columnas y alineadas a la izquierda.
 */
public class Ejercicio3 {
    public static void main(String[] args) {
        System.out.printf("%-10s %-10s\n", "manzana", "apple");
        System.out.printf("%-10s %-10s\n", "aguacate", "avocado");
        System.out.printf("%-10s %-10s\n", "pera", "pear");
        System.out.printf("%-10s %-10s\n", "uvas", "grapes");
        System.out.printf("%-10s %-10s\n", "guayaba", "guava");
        System.out.printf("%-10s %-10s\n", "piña", "pineapple");
        System.out.printf("%-10s %-10s\n", "lemon", "limon");
        System.out.printf("%-10s %-10s\n", "gengibre", "ginger");
        System.out.printf("%-10s %-10s\n", "sandia", "watermelon");
        System.out.printf("%-10s %-10s\n", "ajo", "garlic");

    }
}
